    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            if(jDateChooser2.getDate()==null){
                JOptionPane.showMessageDialog(this,"Tidak bisa ambil laundry.\nlaundry Sedang Dalam Proses");
            }
            if(jTextField4.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Isikan Nama Pengambil");
                jTextField4.requestFocus(true);
                jTextField4.setBackground(Color.red);
            }
            else{
                String [] kolom = { "status_bayar", "tanggal_ambil","nama_pengambil"};
                String [] isi = {(String)jComboBox1.getSelectedItem(), format.format(jDateChooser1.getDate()),
                    jTextField4.getText()
                };
                System.out.println(dbCon.queryUpdate("laundry", kolom, isi, "id_transaksi ='"+jTextField1.getText()+"'"));
                JOptionPane.showMessageDialog(this,"Data Sukses Di Simpan");
                String reportName = "ambilLaundry";
                String [][] param = {
                    {"kode", jTextField1.getText()}
                };
                dbCon.createReport(reportName, param,true);

            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Gagal eksekusi data");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

