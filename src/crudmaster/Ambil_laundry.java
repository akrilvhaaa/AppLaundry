    private void jTable2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable2KeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            coltbl = jTable2.getSelectedRow();
            jTextField1.setText(String.valueOf(jTable2.getValueAt(coltbl, 0)));
            jTextField2.setText(String.valueOf(jTable2.getValueAt(coltbl, 1)));
            Date tgl_selesai = (Date)jTable2.getValueAt(coltbl, 3);
            jDateChooser2.setDate(tgl_selesai);
            jFrame1.dispose();

            if(tgl_selesai==null){
                JOptionPane.showMessageDialog(this,"\nlaundry Sedang Dalam Proses Pengerjaan");
            }

            loadData_Transaksi();
        }
    }//GEN-LAST:event_jTable2KeyPressed

