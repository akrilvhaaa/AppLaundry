     public void loadData_Transaksi(){
         try {
             String nota = jTextField1.getText();

                    rs = dbCon.eksekusiQuery("select * from view_laundry "
                            + "where id_transaksi like '%"+nota+"%'");
                    while(rs.next()){
                        jTextField2.setText(rs.getString("nama_pelanggan"));
                        jLabel21.setText(rs.getString("sisa_bayar"));
                        jLabel22.setText(rs.getString("uang_muka"));
                        jDateChooser2.setDate(rs.getDate("tanggal_selesai"));
                        String status = rs.getString("status_bayar");
                        if(jLabel22.getText() == "0" || jLabel21.getText() != "0" || status == "Belum Bayar" || status == "Sisa"){
                            jComboBox1.setSelectedIndex(0);
                        }
                        else if(jLabel21.getText() == "0"){
                            jComboBox1.setSelectedIndex(1);
                        }
                        if(jCheckBox1.isSelected()){
                            jTextField4.setText(rs.getString("nama_pelanggan"));
                        }
                    }
         } catch (Exception e) {
         }
     }
     
